    def test_should_update_entity_voiceovers_model(self) -> None:
        manual_voiceover_dict_1: state_domain.VoiceoverDict = {
            'filename': 'filename1.mp3',
            'file_size_bytes': 3000,
            'needs_update': False,
            'duration_secs': 6.1,
        }
        manual_voiceover_dict_2: state_domain.VoiceoverDict = {
            'filename': 'filename2.mp3',
            'file_size_bytes': 3500,
            'needs_update': False,
            'duration_secs': 5.9,
        }
        manual_voiceover_dict_3: state_domain.VoiceoverDict = {
            'filename': 'filename3.mp3',
            'file_size_bytes': 4000,
            'needs_update': False,
            'duration_secs': 7.2,
        }
        manual_voiceover_dict_4: state_domain.VoiceoverDict = {
            'filename': 'filename4.mp3',
            'file_size_bytes': 4000,
            'needs_update': False,
            'duration_secs': 7.2,
        }

        content_id_to_manual_voiceovers = {
            'content_0': manual_voiceover_dict_1,
            'content_1': manual_voiceover_dict_2,
            'content_2': manual_voiceover_dict_3,
        }

        entity_voiceovers_model = (
            voiceover_models.EntityVoiceoversModel.create_new(
                feconf.ENTITY_TYPE_EXPLORATION,
                'exp_1',
                1,
                'en-US',
                {
                    'content_0': {
                        'manual': manual_voiceover_dict_4,
                        'auto': None,
                    }
                },
                {},
            )
        )

        updated_entity_voiceovers_model = (
            exploration_voiceover_sync_jobs.update_entity_voiceovers_model(
                content_id_to_manual_voiceovers, entity_voiceovers_model
            )
        )

        expected_voiceovers_mapping = {
            'content_0': {'manual': manual_voiceover_dict_1, 'auto': None},
            'content_1': {'manual': manual_voiceover_dict_2, 'auto': None},
            'content_2': {'manual': manual_voiceover_dict_3, 'auto': None},
        }

        self.assertEqual(
            updated_entity_voiceovers_model.voiceovers_mapping,
            expected_voiceovers_mapping,
        )

